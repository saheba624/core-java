package BasicJava;

public class Substraction_Program {
	
	public static void main(String[] args) {
		int i=21;
		int j=9;
		
		int sum=i+j;
		int sub=i-j;
		int multiple=i*j;
		int division=i%j;
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(multiple);
		System.out.println(division);
		
	}
		
		
	}


